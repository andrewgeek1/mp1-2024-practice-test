// Задача 1. Дан непустой массив A, содержащий n вещественных чисел.
// Вернуть 1, если его элементы образуют геометрическую прогрессию,
// иначе вернуть 0.

  #include <stdio.h>
int task1(double A[], size_t n) {
    if (n == 0 || A[0] == 0) { // Пустые массивы или массивы с нулевыми элементами не могут образовывать прогрессию
        return 0;
    }
    
    double q = A[1] / A[0]; // Знаменатель прогрессии
    
    for (size_t i = 1; i < n; ++i) {
        if (A[i] != A[i-1] * q) { // Проверяем, совпадает ли текущее отношение со знаменателем
            return 0;
        }
    }
    
    return 1; // Все отношения совпадают, значит это геометрическая прогрессия
}
